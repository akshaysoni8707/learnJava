package Chapter1;

public class DataTypes {
    public static void main(String[] args) {
        boolean first = true;
        /// int,float,long,double,char    primitive data type
        /// Integer,Float,Long,Double,Character   non primitive data types
        int a = 1000;
        Integer a1 = 2000;
        long b = 10L;
        Long b1 = 3210L;
        float c = 2.35f;
        Float c1 = 3.56f;
        double d = 12.55;
        Double d1 = 23.54;
        char e = 'X';
        Character e1 = 'T';
        String f = "Java";
        System.out.println(Integer.max(5, 9));
        System.out.println("int is " + a);
        System.out.println("long is " + b);
        System.out.println("float is " + c);
        System.out.println("double is " + d);
        System.out.println("character is " + e);
        System.out.println("string is " + f);
    }
}
